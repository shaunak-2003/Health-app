/**
 *  @swagger
 *  /api/food_entries:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Food_entries]
 *      summary: Get all food_entries
 *      description: Get all food_entries
 *      responses:
 *        200:
 *          description: Food_entries list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Food_entries"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */

router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;

    const payload = await Food_entriesDBApi.findAll(req.query);
    if (filetype && filetype === 'csv') {
      const fields = [
        'id',

        'calorie_count',
        'fat_content',
        'protein_content',
        'carbohydrate_content',
        'entry_date',
      ];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

